package eu.engys.util.filechooser.favorites;

public class Favorite {

    public enum Type {
        USER, SYSTEM, JVFSFILECHOOSER;

        public boolean isSystem() {
            return equals(SYSTEM);
        }
        
        public boolean isUser() {
            return equals(USER);
        }
    }

    private String name;

    private Type type = Type.USER;

    private String url;

    public Favorite(String name, String url, Type type) {
        this.name = name;
        this.type = type;
        this.url = url;

    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return name + " [" + url + "]";
    }

}
