package eu.engys.util.filechooser.table.renderer;

import java.awt.Component;

import javax.swing.JLabel;
import javax.swing.JTable;
import javax.swing.SwingConstants;
import javax.swing.table.DefaultTableCellRenderer;

import eu.engys.util.filechooser.table.FileSize;

public class FileSizeTableCellRenderer extends DefaultTableCellRenderer {

    @Override
    public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        FileSize valueAt = (FileSize) table.getValueAt(row, column);
        label.setText(valueAt.toString() + " ");
        label.setHorizontalAlignment(SwingConstants.RIGHT);

        return label;
    }

}
