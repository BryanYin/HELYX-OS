package eu.engys.gui.solver.postprocessing.data;

import javax.vecmath.Point3d;

import eu.engys.core.project.system.monitoringfunctionobjects.TimeBlockUnit;

public class PointTimeBlockUnit extends TimeBlockUnit {

    private Point3d point;

    public PointTimeBlockUnit(String varName, double[] points) {
        super(varName);
        this.point = new Point3d(points);
    }

    public PointTimeBlockUnit(String varName, Point3d point) {
        super(varName);
        this.point = point;
    }

    public Point3d getPoint() {
        return point;
    }

    @Override
    public String toString() {
        return "[" + getVarName() + ", " + getPoint() + "]";
    }
}