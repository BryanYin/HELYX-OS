package eu.engys.core.project;

import java.util.ArrayList;
import java.util.List;
import java.util.Set;

import javax.inject.Inject;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import eu.engys.core.modules.ApplicationModule;
import eu.engys.util.progress.ProgressMonitor;

public abstract class AbstractProjectWriter implements ProjectWriter {

	protected static final Logger logger = LoggerFactory.getLogger(ProjectWriter.class);
	
	protected final Model model;
	protected final ProgressMonitor monitor;
	protected final Set<ApplicationModule> modules;

	protected List<ProjectWriter> writers = new ArrayList<>();

	@Inject
	public AbstractProjectWriter(Model model, Set<ApplicationModule> modules, ProgressMonitor monitor) {
		this.model = model;
		this.modules = modules;
		this.monitor = monitor;
	}
	
	@Override
	public void registerWriter(ProjectWriter writer) {
		if (writer != null) 
			writers.add(writer); 
	}

}
